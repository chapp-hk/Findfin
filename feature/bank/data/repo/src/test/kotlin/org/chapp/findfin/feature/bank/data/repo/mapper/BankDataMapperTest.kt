    )
    @EnumSource(TypePath::class)
    fun testBankRemoteToBankLocal(input: TypePath) {
        val bankResponse =
            BankRemote(
                district = "mock area",
                bankName = "mock bank name",
                typeName = "bank",
                address = "mock address",
                serviceHours = "24 hours",
                latitude = 0.0,
                longitude = 0.0,
            )

        bankResponse.toBankLocal(
            language = "en",
            type = input,
        ) shouldBe
            BankLocal(
                language = "en",
                type = input.name,
                district = "mock area",
                bankName = "mock bank name",
                typeName = "bank",
                address = "mock address",
                serviceHours = "24 hours",
                latitude = 0.0,
                longitude = 0.0,
            )
    }

